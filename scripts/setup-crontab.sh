#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ crontab –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∏ —Ä–µ–∑–µ—Ä–≤–Ω–æ–≥–æ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è

# –ù–∞—Å—Ç—Ä–æ–π–∫–∏
SERVER_HOST="root@2a03:6f00:a::9409"
SSH_KEY="~/.ssh/id_rsa"
DEPLOY_PATH="/root/lip-workshop"

# –ü–∞—Ä–æ–ª—å –¥–ª—è root
ROOT_PASSWORD="yfu4MemdN+o-6M"

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m' # No Color

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —Å —Ü–≤–µ—Ç–æ–º
echo_c() {
  echo -e "${2}${1}${NC}"
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –∫–æ–º–∞–Ω–¥ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ
ssh_command() {
  ssh -i $SSH_KEY $SERVER_HOST "$1"
}

# –ù–∞—á–∞–ª–æ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏
echo_c "‚è∞ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –∑–∞–¥–∞—á –¥–ª—è –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –õ–ò–ü..." "$YELLOW"
echo

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è —Å —Å–µ—Ä–≤–µ—Ä–æ–º
echo_c "–ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è —Å —Å–µ—Ä–≤–µ—Ä–æ–º..." "$YELLOW"
if ssh -i $SSH_KEY -q $SERVER_HOST exit; then
  echo_c "‚úÖ –°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ —Å —Å–µ—Ä–≤–µ—Ä–æ–º —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ." "$GREEN"
else
  echo_c "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ —Å–µ—Ä–≤–µ—Ä—É. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è." "$RED"
  exit 1
fi
echo

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è —Å–∫—Ä–∏–ø—Ç–æ–≤ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∏ —Ä–µ–∑–µ—Ä–≤–Ω–æ–≥–æ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ
echo_c "–ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è —Å–∫—Ä–∏–ø—Ç–æ–≤ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ..." "$YELLOW"
MONITORING_EXISTS=$(ssh_command "test -f $DEPLOY_PATH/scripts/monitoring.sh && echo 1 || echo 0")
BACKUP_EXISTS=$(ssh_command "test -f $DEPLOY_PATH/scripts/backup.sh && echo 1 || echo 0")

if [[ "$MONITORING_EXISTS" == "0" || "$BACKUP_EXISTS" == "0" ]]; then
  echo_c "‚ùå –ù–µ–∫–æ—Ç–æ—Ä—ã–µ —Å–∫—Ä–∏–ø—Ç—ã –æ—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ. –ö–æ–ø–∏—Ä—É—é –∏—Ö..." "$RED"
  
  # –°–æ–∑–¥–∞–µ–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –¥–ª—è —Å–∫—Ä–∏–ø—Ç–æ–≤ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ, –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
  ssh_command "mkdir -p $DEPLOY_PATH/scripts"
  
  # –ö–æ–ø–∏—Ä—É–µ–º —Å–∫—Ä–∏–ø—Ç—ã –Ω–∞ —Å–µ—Ä–≤–µ—Ä
  scp -i $SSH_KEY ./scripts/monitoring.sh "$SERVER_HOST:$DEPLOY_PATH/scripts/"
  scp -i $SSH_KEY ./scripts/backup.sh "$SERVER_HOST:$DEPLOY_PATH/scripts/"
  
  # –î–µ–ª–∞–µ–º —Å–∫—Ä–∏–ø—Ç—ã –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º–∏
  ssh_command "chmod +x $DEPLOY_PATH/scripts/monitoring.sh"
  ssh_command "chmod +x $DEPLOY_PATH/scripts/backup.sh"
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —Å–∫—Ä–∏–ø—Ç—ã —Ç–µ–ø–µ—Ä—å –µ—Å—Ç—å –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ
MONITORING_EXISTS=$(ssh_command "test -f $DEPLOY_PATH/scripts/monitoring.sh && echo 1 || echo 0")
BACKUP_EXISTS=$(ssh_command "test -f $DEPLOY_PATH/scripts/backup.sh && echo 1 || echo 0")

if [[ "$MONITORING_EXISTS" == "1" && "$BACKUP_EXISTS" == "1" ]]; then
  echo_c "‚úÖ –í—Å–µ –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ —Å–∫—Ä–∏–ø—Ç—ã –ø—Ä–∏—Å—É—Ç—Å—Ç–≤—É—é—Ç –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ." "$GREEN"
else
  echo_c "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–µ—Å–ø–µ—á–∏—Ç—å –Ω–∞–ª–∏—á–∏–µ –≤—Å–µ—Ö —Å–∫—Ä–∏–ø—Ç–æ–≤ –Ω–∞ —Å–µ—Ä–≤–µ—Ä–µ." "$RED"
  exit 1
fi
echo

# –ù–∞—Å—Ç—Ä–æ–π–∫–∞ crontab
echo_c "–ù–∞—Å—Ç—Ä–æ–π–∫–∞ crontab –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –∑–∞–¥–∞—á..." "$YELLOW"

# –°–æ–∑–¥–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—ã–π —Ñ–∞–π–ª crontab
TEMP_CRONTAB=$(mktemp)

# –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–∏–µ –∑–∞–¥–∞–Ω–∏—è crontab —Å —Å–µ—Ä–≤–µ—Ä–∞
ssh_command "crontab -l 2>/dev/null" > $TEMP_CRONTAB

# –î–æ–±–∞–≤–ª—è–µ–º –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏ –¥–ª—è –õ–ò–ü
echo "# –õ–ò–ü - –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–µ –∑–∞–¥–∞—á–∏" >> $TEMP_CRONTAB

# –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–¥–∞–Ω–∏–µ –¥–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ (–∫–∞–∂–¥—ã–π —á–∞—Å)
echo "0 * * * * $DEPLOY_PATH/scripts/monitoring.sh > /dev/null 2>&1" >> $TEMP_CRONTAB

# –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–¥–∞–Ω–∏–µ –¥–ª—è —Ä–µ–∑–µ—Ä–≤–Ω–æ–≥–æ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è (–µ–∂–µ–¥–Ω–µ–≤–Ω–æ –≤ 2:00)
echo "0 2 * * * $DEPLOY_PATH/scripts/backup.sh > /dev/null 2>&1" >> $TEMP_CRONTAB

# –ó–∞–≥—Ä—É–∂–∞–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–π crontab –Ω–∞ —Å–µ—Ä–≤–µ—Ä
cat $TEMP_CRONTAB | ssh -i $SSH_KEY $SERVER_HOST "crontab -"

# –£–¥–∞–ª—è–µ–º –≤—Ä–µ–º–µ–Ω–Ω—ã–π —Ñ–∞–π–ª
rm $TEMP_CRONTAB

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∑–∞–¥–∞–Ω–∏—è –¥–æ–±–∞–≤–ª–µ–Ω—ã
CRONTAB_CHECK=$(ssh_command "crontab -l | grep -c '–õ–ò–ü'")
if [[ "$CRONTAB_CHECK" -gt 0 ]]; then
  echo_c "‚úÖ –ó–∞–¥–∞–Ω–∏—è crontab —É—Å–ø–µ—à–Ω–æ –Ω–∞—Å—Ç—Ä–æ–µ–Ω—ã." "$GREEN"
  ssh_command "crontab -l | grep '–õ–ò–ü' -A3"
else
  echo_c "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞—Å—Ç—Ä–æ–∏—Ç—å –∑–∞–¥–∞–Ω–∏—è crontab." "$RED"
  exit 1
fi
echo

echo_c "üéâ –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏—Ö –∑–∞–¥–∞—á —É—Å–ø–µ—à–Ω–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!" "$GREEN"
echo_c "–ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è –∫–∞–∂–¥—ã–π —á–∞—Å." "$GREEN"
echo_c "–†–µ–∑–µ—Ä–≤–Ω–æ–µ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è –µ–∂–µ–¥–Ω–µ–≤–Ω–æ –≤ 2:00." "$GREEN" 